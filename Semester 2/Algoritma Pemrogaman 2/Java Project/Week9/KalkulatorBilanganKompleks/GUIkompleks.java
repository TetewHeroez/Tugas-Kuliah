    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if(evt.getStateChange()==java.awt.event.ItemEvent.SELECTED){
            if(!jComboBox1.getSelectedItem().toString().equals("--Pilih background display")){
                if(jComboBox1.getSelectedItem().toString().equals("Merah")){
                    jTextArea1.setBackground(Color.red);
                }
                else if(jComboBox1.getSelectedItem().toString().equals("Biru")){
                    jTextArea1.setBackground(Color.blue);
                }
                else if(jComboBox1.getSelectedItem().toString().equals("Kuning")){
                    jTextArea1.setBackground(Color.yellow);
                }
                else if(jComboBox1.getSelectedItem().toString().equals("Hijau")){
                    jTextArea1.setBackground(Color.green);
                }
                else if(jComboBox1.getSelectedItem().toString().equals("Putih")){
                jTextArea1.setBackground(Color.white);
                }
            }
            
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

