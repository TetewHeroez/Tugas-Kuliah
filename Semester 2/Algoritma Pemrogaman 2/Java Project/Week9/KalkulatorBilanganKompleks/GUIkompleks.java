    private void jSquareRootButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSquareRootButtonActionPerformed
        String Rz1 = fieldZ1.getText();
        String Iz1 = fieldZ4.getText();
        String Rz2 = fieldZ3.getText();
        String Iz2 = fieldZ5.getText();

        this.z1.setRe(Double.parseDouble(Rz1));
        this.z1.setIm(Double.parseDouble(Iz1));
        this.z2.setRe(Double.parseDouble(Rz2));
        this.z2.setIm(Double.parseDouble(Iz2));

        ArrayList<BilanganKompleks> squareRoots;
        StringBuilder sb = new StringBuilder();
        String selectedComplexNumberDisplay = "";
    
        if (jRadioButton1.isSelected()) {
            squareRoots = this.z1.squareRoot();
            selectedComplexNumberDisplay = this.z1.Display();
            for (BilanganKompleks root : squareRoots) {
                sb.append(root.Display()).append("\n");
            }
        } else if (jRadioButton2.isSelected()) {
            squareRoots = this.z2.squareRoot();
            selectedComplexNumberDisplay = this.z2.Display();
            for (BilanganKompleks root : squareRoots) {
                sb.append(root.Display()).append("\n");
            }
        } else if (jRadioButton3.isSelected()) {
            squareRoots = this.hasil.squareRoot();
            selectedComplexNumberDisplay = this.hasil.Display();
            for (BilanganKompleks root : squareRoots) {
                sb.append(root.Display()).append("\n");
            }
        }
        jTextArea1.setText("Square root dari " + selectedComplexNumberDisplay + "\n=\n" + sb.toString());
        fieldZ6.setText(sb.toString());
    }//GEN-LAST:event_jSquareRootButtonActionPerformed

